package calculate

func Addition(number_a, number_b int) int {
	result := number_a + number_b

	return result
}
func Substrac(number_a, number_b int) int {
	result := number_a - number_b

	return result
}
func Mult(number_a, number_b int) int {
	result := number_a * number_b

	return result
}
func Divi(number_a, number_b int) int {
	result := number_a / number_b

	return result
}
